const mongoose = require("mongoose");

const sportcategorySchema = new mongoose.Schema(
    {
        name: {
            type: String,
            trim: true,
        },
        description: {
            type: String,
            trim: true,
        },
        is_active: {
            type: Boolean,
            default: true,
        }
    },
    {
        timestamps: true,
        versionkey: false
    }
);

const Sportcategory = mongoose.model("category", sportcategorySchema);

module.exports = Sportcategory;